@app.route("/update-price/<int:id>", methods=["PATCH"])
def updatePrice(id):
    find_cafe_from_id = Cafe.query.get(id)
    if find_cafe_from_id:
        find_cafe_from_id.coffee_price = request.args.get("coffee_price")
        db.session.commit()
        return jsonify(response={"Success": "Successfully changed the coffee price"})
    else:
        return jsonify(response={"Failure": "Not Changed"})

    # HTTP DELETE - Delete Record


